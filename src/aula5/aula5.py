"""
+ = Adição
- = Subtração
* = Multiplicação
/ = Divisão
// = Divisão inteira
** = potenciação
% = Resto
() = Muda a priorização
"""

print('Multiplicação *', 10 * 10)
print('Adição +', 10 + 10)
print('Subtração -', 10 - 5)
print('Divisão /', 10 / 10)

print(10 * '10')

print(10.5 / 3)
print(10.5 // 3)
print(10 % 3)
print(2 ** 3)

print(5+2*10)
print((5+2)*10)
